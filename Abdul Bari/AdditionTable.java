import java.util.Scanner;


    public static void main(String[] args){

        Scanner sc = new Scanner(System.in);

        System.out.println("Enter the initial number: ");
        int n = sc.nextInt();
        int sum = 0 ;
        for(int i = 1; i<=n ; i++ ){
            int summ = summ + i;

        }
        System.out.println("Sum of " + n + "Number is " + summ);
    }

